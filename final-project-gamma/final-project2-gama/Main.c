// Random Number Generator (used website referenced in project document -> https://www.maximintegrated.com/cn/app-notes/index.mvp/id/4400)

typedef unsigned int uint;
uint lfsr31, lfsr32;

int shift_lfsr(uint *lfsr, uint polynomial_mask) {
    int feedback;

    feedback = *lfsr &1;
    *lfsr >>= 1;
    if(feedback == 1)
        *lfsr ^= polynomial_mask;
    return *lfsr;
}

void init_lfsrs(void) {
    lfsr32 = 0xABCDEFAB; // 1st Seed Value
    lfsr31 = NVIC_ST_CURRENT_R; // 2nd Seed Value
}

int RandomCube(void) { // return random number from 0 - 5 that corresponds to the amount of cubes on the LCD screen
    shift_lfsr(&lfsr32, POLY_MASK_32);
    return ((shift_lfsr(&lfsr32, POLY_MASK_32) ^ shift_lfsr(&lfsr31, POLY_MASK_31)) & 0xFFFF) % 6;
}

int Random(void) { // returns random number from 0 - 4 that will correspond to ....
    shift_lfsr(&lfsr32, POLY_MASK_32); // random number from 0-1
    return ((shift_lfsr(&lfsr32, POLY_MASK_32) ^ shift_lfsr(&lfsr31, POLY_MASK_31)) & 0xFFFF) % 2;
}

int RandomDirection(void) { // returns random number from 0 - 3 that will correspond to the direction the cube will move next
    shift_lfsr(&lfsr32, POLY_MASK_32);
    return ((shift_lfsr(&lfsr32, POLY_MASK_32) ^ shift_lfsr(&lfsr31, POLY_MASK_31)) & 0xFFFF) % 4;
}

int RandomColor(void) {  // returns random number from 0 - 7 that will correspond to the index of the global array that contains the colors
    shift_lfsr(&lfsr32, POLY_MASK_32);
    return ((shift_lfsr(&lfsr32, POLY_MASK_32) ^ shift_lfsr(&lfsr31, POLY_MASK_31)) & 0xFFFF) % 8;
}


